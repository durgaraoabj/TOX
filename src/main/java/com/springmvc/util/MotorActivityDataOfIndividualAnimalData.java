package com.springmvc.util;

public class MotorActivityDataOfIndividualAnimalData {
	private String animalNo;
	private String gender;
	private String total;
	private String ambulatory;
	private String stereotypic;
	
	public String getAnimalNo() {
		return animalNo;
	}
	public void setAnimalNo(String animalNo) {
		this.animalNo = animalNo;
	}
	public String getGender() {
		return gender;
	}
	public void setGender(String gender) {
		this.gender = gender;
	}
	public String getTotal() {
		return total;
	}
	public void setTotal(String total) {
		this.total = total;
	}
	public String getAmbulatory() {
		return ambulatory;
	}
	public void setAmbulatory(String ambulatory) {
		this.ambulatory = ambulatory;
	}
	public String getStereotypic() {
		return stereotypic;
	}
	public void setStereotypic(String stereotypic) {
		this.stereotypic = stereotypic;
	}
	
	
}
